package whilecounter;

/**
 *
 * @author Mumu
 */
public class WhileCounter 
{
    public static void main(String[] args) 
    {
        int counter=1;
        while(counter<=100)
        {
            System.out.printf("%d ",counter);
            counter++;
        }
        System.out.println();
    }
    
}
